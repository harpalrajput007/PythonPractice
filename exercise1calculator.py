a = int(input("Enter first number ="))
b = int(input("Enter second number ="))

add = a + b
print("Sum of two numbers is = ", add)

sub = a - b
print("Subtraction of two numbers is = ", sub)

mul = a * b
print("Multiplication of two numbers is = ", mul)

div = a / b
print("Division of two numbers is = ", div)

modu = a % b
print("Modulus of two numbers is = ", modu)

expo = a ** b
print("Exponentiation of two numbers is = ", expo)

floor = a // b
print("Floor division of two numbers is = ", floor)